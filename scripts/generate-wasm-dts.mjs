#!/usr/bin/env node

import { readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = join(__dirname, '..');
const moonPkgPath = join(projectRoot, 'moon.pkg');
const dtsPath = join(projectRoot, 'npm', 'index.d.ts');

const markerStart = '// AUTO-GENERATED:WASM-FUNCTIONS START';
const markerEnd = '// AUTO-GENERATED:WASM-FUNCTIONS END';

const declarationByExportName = {
  parseGrib2: 'export function parseGrib2(data: Uint8Array): number;',
  getMessageCount: 'export function getMessageCount(handle: number): number;',
  getRecordCount: 'export function getRecordCount(handle: number): number;',
  getSection1:
    'export function getSection1(handle: number, messageIndex: number): Section1Data;',
  getSection3:
    'export function getSection3(handle: number, recordIndex: number): Section3Data;',
  getSection4:
    'export function getSection4(handle: number, recordIndex: number): Section4Data;',
  getSection5:
    'export function getSection5(handle: number, recordIndex: number): Section5Data;',
  getSection6:
    'export function getSection6(handle: number, recordIndex: number): Section6Data;',
  getLatitudes:
    'export function getLatitudes(handle: number, recordIndex: number): Float32Array;',
  getLongitudes:
    'export function getLongitudes(handle: number, recordIndex: number): Float32Array;',
  getGridData:
    'export function getGridData(handle: number, recordIndex: number): Float32Array;'
};

function parseMoonPkgExports(moonPkgText) {
  const exportsBlockMatch = moonPkgText.match(
    /"exports"\s*:\s*\[(?<body>[\s\S]*?)\]/m
  );
  if (!exportsBlockMatch?.groups?.body) {
    throw new Error('Failed to find options.link."wasm-gc".exports in moon.pkg');
  }

  const lines = exportsBlockMatch.groups.body
    .split('\n')
    .map((line) => line.trim())
    .filter(
      (line) =>
        line.startsWith('"') &&
        line.includes(':') &&
        (line.endsWith('",') || line.endsWith('"'))
    );

  return lines.map((line) => {
    const raw = line.endsWith('",') ? line.slice(1, -2) : line.slice(1, -1);
    const parts = raw.split(':');
    if (parts.length < 2) {
      throw new Error(`Invalid export line in moon.pkg: ${line}`);
    }
    return {
      moonName: parts[0],
      jsName: parts[1]
    };
  });
}

function generateDeclarations(moonExports) {
  const declarations = [
    'export function init(): Promise<void>;',
    'export function initBrowser(wasmUrl: string | URL): Promise<void>;'
  ];

  for (const entry of moonExports) {
    const decl = declarationByExportName[entry.jsName];
    if (!decl) {
      throw new Error(
        `No TypeScript declaration mapping for exported function "${entry.jsName}" (from ${entry.moonName})`
      );
    }
    declarations.push(decl);
  }
  return declarations;
}

function replaceGeneratedBlock(dtsText, declarations) {
  const start = dtsText.indexOf(markerStart);
  const end = dtsText.indexOf(markerEnd);
  if (start === -1 || end === -1 || end < start) {
    throw new Error(
      `Markers not found in ${dtsPath}. Expected ${markerStart} ... ${markerEnd}`
    );
  }

  const before = dtsText.slice(0, start + markerStart.length);
  const after = dtsText.slice(end);
  const generated = `\n// This block is generated by scripts/generate-wasm-dts.mjs.\n// Do not edit manually.\n${declarations.join('\n')}\n`;
  return `${before}${generated}${after}`;
}

function main() {
  const moonPkgText = readFileSync(moonPkgPath, 'utf8');
  const dtsText = readFileSync(dtsPath, 'utf8');

  const moonExports = parseMoonPkgExports(moonPkgText);
  const declarations = generateDeclarations(moonExports);
  const updated = replaceGeneratedBlock(dtsText, declarations);

  if (updated !== dtsText) {
    writeFileSync(dtsPath, updated, 'utf8');
    console.log(`Updated ${dtsPath}`);
  } else {
    console.log(`${dtsPath} is up to date`);
  }
}

main();
